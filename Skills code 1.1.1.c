task autonomous()
{
	resetMotorEncoder(rightMotor);
	resetMotorEncoder(leftMotor);
while(nMotorEncoder[leftMotor] > -1200)
	{
		motor[rightMotor] = motor[leftMotor] = -127;
	}
		motor[rightMotor] = motor[leftMotor] = -127;

for(int x =0; x< 4; x++) {
	resetMotorEncoder(rightMotor);
	resetMotorEncoder(leftMotor);

		motor[claw1] = motor[claw2] = 120;			//close claw on objects
		wait1Msec(1500);
		motor[claw1] = motor[claw2] = 50;

	resetMotorEncoder(rightMotor);
while(nMotorEncoder[rightMotor] > -2400)	//backwards to fence
	{
		motor[rightMotor] = motor[leftMotor] = -127;
	}
		motor[rightMotor] = motor[leftMotor] = 0;
		wait1Msec(10);

while(getMotorEncoder(lift1) < 1000)			//lift up almost to top
	{
		motor[lift1] = motor[lift2] = 127;
	}
		wait1Msec(1000);

		motor[lift1] = motor[lift2] = 127;
		motor[claw1] = motor[claw2] = -127;
		wait1Msec(200);												//finishes lift raise, and lets go of cube

		motor[claw1] = motor[claw2] = 0;

while(nMotorEncoder(lift1) > 10)					//lowers lift
	{
		motor[lift1] = motor[lift2] = -127;
	}
		motor[lift1] = motor[lift2] = -127;
		wait1Msec(10);

		motor[claw1] = motor[claw2] = -127;			// opens claw
		wait1Msec(2000);
		motor[claw1] = motor[claw2] = 0;

	resetMotorEncoder(rightMotor);
while(nMotorEncoder(rightMotor) < 2400)			//return to loop starting position
		{
			motor[rightMotor] = motor[leftMotor] = 127;
		}
		motor[rightMotor] = motor[leftMotor] = 0;
		wait1Msec(10);
	}



	resetMotorEncoder(rightMotor);
	resetMotorEncoder(leftMotor);

while(nMotorEncoder[rightMotor] > -2400)
		{
			motor[rightMotor] = motor[leftMotor] = -127;
		}
			motor[rightMotor] = motor[leftMotor] = 0;
		resetMotorEncoder(rightMotor);

while(nMotorEncoder[rightMotor] < 1375)
		{
			motor[rightMotor] = 127;
		}
	resetMotorEncoder(rightMotor);
while(nMotorEncoder[rightMotor] < 1200)
		{
			motor[rightMotor] = motor[leftMotor] = 127;
		}
			motor[rightMotor] = motor[leftMotor] = 0;

			motor[claw1] = motor[claw2] = 110;
			wait1Msec(2000);//close claw on cube

			motor[lift1] = motor[lift2] = 80;
			wait1Msec(500);
			motor[lift1] = motor[lift2] = 20;


		resetMotorEncoder(rightMotor);
while(nMotorEncoder[rightMotor] > -1375)//turns around, clockwise, back to fence
		{
			motor[rightMotor] = -127;
		}
			motor[rightMotor] = 0;
			wait1Msec(100);

		resetMotorEncoder(rightMotor);
while(nMotorEncoder[rightMotor] > -1254)//backs up to fence
{
			motor[rightMotor] = motor[leftMotor] = -110;
}
			motor[rightMotor] = motor[leftMotor] = 0;
			wait1Msec(100);

while(getMotorEncoder(lift1) < 1200)//lift up almost to top
	{
			motor[lift1] = motor[lift2] = 127;
	}
			motor[lift1] = motor[lift2] = 15;
			wait1Msec(10);

			motor[lift1] = motor[lift2] = 127;
			motor[claw1] = motor[claw2] = -127;
			wait1Msec(750);//finishes lift raise, and lets go of cube

			motor[lift1] = motor[lift2] = 0;
			motor[claw1] = motor[claw2] = 0;
			motor[rightMotor] = motor[leftMotor] = 0;
}

